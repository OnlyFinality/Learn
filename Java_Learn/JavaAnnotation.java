package Java_Learn;

public class JavaAnnotation {
    public static void main(String[] args) {
        // 这是一个单行注释
        int x = 10; // 初始化一个变量x为10
        /*
        这是一个多行注释
        可以用来注释多行代码
        */
        int y = 20; // 初始化一个变量y为20
    }
    /**
     * 这是一个文档注释示例
     * 它通常包含有关类、方法或字段的详细信息
     */
    public class MyClass {
        // 类的成员和方法
    }
}
